#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	unsigned char			*d;
	unsigned const char		*s;

	d = dst;
	s = src;
	if (!dst && !src)
		return (dst);
	if (d < s)
		ft_memcpy(dst, src, len);
	else
	{
		while (len > 0)
		{
			d[len - 1] = s[len - 1];
			len--;
		}
	}
	return ((void *)dst);
}
